                    <h3 class="card-subtitle mb-4 ">Instructions</h3>
                    <ol class="list-unstyled text-start">
                        <li>1.Mix the glutinous rice flour, rice flour, and salt well.Add hot water one spoon at a time and knead.</li>
                        <li>2.Knead the rice flour into small pieces with your hands and filter it.</li>
                        <li>3.Sift the rice flour into a small bowl.For food, crush small stones and do not break them.</li>
                        <li>4..Lightly grease the mixing bowls. Add one tablespoon of the sifted flour first, then add one tablespoon of the flour that has been pounded with a palm.</li>
                        <li>5..Sprinkle about two tablespoons of flour on top to coat the food.Now add the powder one cup at a time until it is all dissolved,then simmer in boiling water for 10 minutes./li>
